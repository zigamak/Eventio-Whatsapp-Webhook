# ─── Extract last 8 digits from each number ───────────────────
def last8(raw):
    digits = re.sub(r'\D', '', raw)
    return digits[-8:] if len(digits) >= 8 else digits

suffixes = sorted(set(last8(n) for n in RAW_NUMBERS.strip().splitlines() if n.strip()))
print(f"Unique last-8-digit suffixes: {len(suffixes)}")

# ─── Fixed date: May 25, 2026 ─────────────────────────────────
target_date = date(2026, 5, 25)
print(f"Query date: {target_date}")

# ─── Build SQL ─────────────────────────────────────────────────
suffix_conditions = " OR ".join(f"wa_id LIKE '%{s}'" for s in suffixes)

query = f"""
    SELECT
        id,
        wa_id,
        name,
        type,
        body,
        timestamp,
        DATE(timestamp) AS message_date,
        direction,
        status,
        read
    FROM public.package_with_sense_messages
    WHERE DATE(timestamp) = '{target_date}'
      AND ({suffix_conditions})
    ORDER BY
        wa_id ASC,
        timestamp ASC;
"""

# ─── Connect & fetch ──────────────────────────────────────────
def get_connection():
    return psycopg2.connect(
        host=os.getenv("DB_HOST"),
        port=os.getenv("DB_PORT", "5432"),
        dbname=os.getenv("DB_NAME"),
        user=os.getenv("DB_USER"),
        password=os.getenv("DB_PASSWORD"),
        sslmode=os.getenv("DB_SSLMODE", "require"),
    )

def ordinal(n):
    suffix = {1:"st",2:"nd",3:"rd"}.get(n % 10 if not 11 <= n % 100 <= 13 else 0, "th")
    return f"{n}{suffix}"

def format_ts(ts):
    if ts is None: return ""
    if isinstance(ts, str):
        try: ts = datetime.fromisoformat(ts)
        except: return ts
    if isinstance(ts, datetime):
        return f"{ordinal(ts.day)} {ts.strftime('%B, %Y')} {ts.strftime('%I:%M %p').lstrip('0')}"
    return f"{ordinal(ts.day)} {ts.strftime('%B, %Y')}"

try:
    conn = get_connection()
    print("✅ Connected to database.")
    with conn.cursor(cursor_factory=RealDictCursor) as cur:
        cur.execute(query)
        rows = cur.fetchall()
    conn.close()
    print(f"✅ Fetched {len(rows)} rows.")
except Exception as e:
    print(f"❌ Error: {e}")
    rows = []

# ─── Export CSV ───────────────────────────────────────────────
if rows:
    filename = f"messages_{target_date}.csv"
    fieldnames = ["wa_id","name","message_date","type","body","direction","status","read"]
    with open(filename, "w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        for row in rows:
            r = dict(row)
            r["message_date"] = format_ts(r.get("message_date"))
            writer.writerow({k: r.get(k, "") for k in fieldnames})
    print(f"✅ Saved to {filename}")
else:
    print("No rows found.")